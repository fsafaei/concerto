r"""Per-task benchmark leaderboard from verified bundles only (ADR-027; ADR-028 §Decision 3).

ADR-027 §Reporting rules: the leaderboard is **per-task tables** (no
scalar composite in v1.0), IQM + 95% cluster-bootstrap CIs, mandatory
per-partner breakdown, no single-seed claims. This module renders one
task's table from its committed result-bundle directories, and it
**refuses unverifiable entries**: every input bundle is re-verified via
:func:`chamber.evaluation.bundles.verify_bundle_dir` (the same gate
``chamber-eval verify`` runs) before a single number is read — an entry
that cannot be recomputed is not a leaderboard row.

Inputs are explicit, never scanned: a task directory (e.g.
``spikes/results/benchmark/cocarry-v1``) commits a
:data:`LEADERBOARD_MANIFEST` file listing its leaderboard bundle
directories one per line (repo-root-relative; ``#`` comments allowed).
Explicitness is the honesty property — power-pilot bundles
(``run_purpose: power``, excluded from results by the campaign prereg)
simply never appear in the manifest, and nothing outside the
preregistered cell list can drift in via a glob.

Row identity comes from each bundle's ``policy_id`` via
:func:`row_id_for_policy` (the ADR-011 v1.0 row names). Rows may span
multiple bundles (B-JOINT ships one pair bundle per training seed);
statistics are recomputed from the pooled raw episodes with the same
deterministic cluster bootstrap the bundle summaries pin. Labels are
mandated by ADR-011 §Decision as amended: REF-SCRIPT renders as
*oracle reference* and B-JOINT as *non-AHT upper anchor* in every
table.

The README section between :data:`README_LEADERBOARD_BEGIN` /
:data:`README_LEADERBOARD_END` is regenerated by
``scripts/render_leaderboard_table.py`` and drift-checked in CI exactly
like the CB-02 task table.
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import TYPE_CHECKING

import numpy as np

from chamber.evaluation.bootstrap import cluster_bootstrap
from chamber.evaluation.bundles import verify_bundle_dir
from chamber.evaluation.results import EpisodeResult, ResultBundle
from concerto.training.seeding import derive_substream

if TYPE_CHECKING:  # pragma: no cover
    from collections.abc import Iterable

#: Committed per-task manifest naming the leaderboard bundle dirs
#: (one repo-root-relative path per line; ``#`` comments allowed).
LEADERBOARD_MANIFEST: str = "LEADERBOARD_BUNDLES.txt"

#: README marker pair (mirrors the CB-02 task-table convention).
README_LEADERBOARD_BEGIN: str = (
    "<!-- CHAMBER-BENCH-LEADERBOARD:BEGIN — generated by "
    "scripts/render_leaderboard_table.py; edit spikes/results/benchmark/, "
    "not this table -->"
)
README_LEADERBOARD_END: str = "<!-- CHAMBER-BENCH-LEADERBOARD:END -->"

#: ADR-011 §Decision (as amended) mandated row labels.
ROW_LABELS: dict[str, str] = {
    "REF-SCRIPT": "oracle reference",
    "B-JOINT": "non-AHT upper anchor",
}

#: Row order in every rendered table (oracle first, then floor→anchor).
ROW_ORDER: tuple[str, ...] = ("REF-SCRIPT", "B-RND", "B-STAT", "B-BLIND", "B-AHT", "B-JOINT")

#: Per-task solvability-anchor footnotes, sourced from the committed
#: admission evidence (ADR-027 §Admission protocol; amended 2026-07-06).
_SOLVABILITY_ANCHORS: dict[str, str] = {
    "cocarry-v1": (
        "*Solvability anchor: matched scripted reference 1.000 (admission "
        "A1, `spikes/results/admission/cocarry-2026-07-05/`).*"
    ),
    "handover-v1": (
        "*Solvability anchor: matched presenter 1.000 (Gate-0 Limb 1, "
        "`spikes/results/handover-place-gate0-2026-06-26/`). Leaderboard "
        "cells are the measured coupling-valid (mismatch) region by design "
        "— the low oracle number is the mismatch penalty, not "
        "unsolvability.*"
    ),
}

#: Estimator-transparency footnote (ADR-027 §Reporting rules; amended
#: 2026-07-06): IQM stays the preregistered headline; the mean column
#: is context, never a re-ranking.
_ESTIMATOR_FOOTNOTE: str = (
    "*Success IQM is the preregistered `iqm_success_rate`: the "
    "interquartile mean over per-episode success indicators; it is "
    "intentionally conservative away from saturation (e.g., handover "
    "REF-SCRIPT mean 0.338 vs IQM 0.176) and trims minority cells at "
    "saturation (e.g., B-AHT mean 0.961 vs IQM 1.000 — see the "
    "per-partner range).*"
)

#: Deterministic bootstrap parameters for the recomputed row statistics
#: (matches the bundle-summary convention, ADR-028 §Decision 1).
_N_RESAMPLES: int = 2000
_BOOTSTRAP_ROOT_SEED: int = 0


def row_id_for_policy(policy_id: str) -> str:
    """Map a bundle ``policy_id`` to its ADR-011 v1.0 row name.

    Unknown policies map to themselves — a non-campaign bundle renders
    under its own id rather than silently joining a named row.
    """
    if policy_id == "random":
        return "B-RND"
    if policy_id == "static":
        return "B-STAT"
    if policy_id in ("ref_script_cocarry_impedance", "ref_script_handover_ego"):
        return "REF-SCRIPT"
    kind = policy_id.partition(":")[0]
    return {
        "happo": "B-AHT",
        "happo_manifest": "B-AHT",
        "happo_blind": "B-BLIND",
        "happo_blind_manifest": "B-BLIND",
        "joint_ego": "B-JOINT",
        "joint_manifest": "B-JOINT",
    }.get(kind, policy_id)


@dataclass(frozen=True)
class LeaderboardRow:
    """One rendered per-task leaderboard row (ADR-027 §Reporting rules).

    Attributes:
        row_id: ADR-011 row name (or the raw policy id for
            non-campaign bundles).
        label: Mandated label (*oracle reference* / *non-AHT upper
            anchor*) or ``""``.
        n_episodes: Pooled episode count across the row's bundles.
        success_mean: Plain mean of per-episode success (estimator
            transparency beside the preregistered IQM headline).
        success_iqm: IQM of per-episode success over the pooled grid.
        success_ci_low: 2.5th percentile of the cluster bootstrap.
        success_ci_high: 97.5th percentile of the cluster bootstrap.
        stress_p90: 90th percentile of the episode stress maxima
            (``force_peak``), N; ``nan`` when the task has no stress
            channel.
        partner_min: ``(member, mean success)`` of the weakest partner
            cell — the per-partner breakdown floor.
        partner_max: ``(member, mean success)`` of the strongest cell.
        seeds: Sorted union of cluster seeds across the row's bundles.
        bundle_paths: The committed bundle dirs backing the row.
    """

    row_id: str
    label: str
    n_episodes: int
    success_mean: float
    success_iqm: float
    success_ci_low: float
    success_ci_high: float
    stress_p90: float
    partner_min: tuple[str, float]
    partner_max: tuple[str, float]
    seeds: list[int]
    bundle_paths: list[str]


class LeaderboardInputError(ValueError):
    """An input bundle failed verification or the manifest is malformed (ADR-028)."""


def read_manifest(task_dir: Path) -> list[Path]:
    """Read the committed bundle list for one task (ADR-027 §Reporting rules).

    Args:
        task_dir: The task's results directory (holding
            :data:`LEADERBOARD_MANIFEST`).

    Returns:
        Repo-root-relative bundle paths, in manifest order.

    Raises:
        LeaderboardInputError: Missing manifest file.
    """
    manifest = task_dir / LEADERBOARD_MANIFEST
    if not manifest.is_file():
        msg = (
            f"{manifest}: no {LEADERBOARD_MANIFEST} — the leaderboard renders "
            "only explicitly committed bundles (ADR-027 §Reporting rules)"
        )
        raise LeaderboardInputError(msg)
    lines = [ln.strip() for ln in manifest.read_text(encoding="utf-8").splitlines()]
    return [Path(line) for line in lines if line and not line.startswith("#")]


def load_verified_bundles(
    bundle_dirs: Iterable[Path], *, repo_path: Path
) -> list[tuple[Path, ResultBundle, list[EpisodeResult]]]:
    """Verify each bundle and load its raw episodes (ADR-028 §Decision 3).

    Every bundle runs the full ``chamber-eval verify`` check table;
    any failing check refuses the entire render — a leaderboard with
    one unverifiable entry is not a leaderboard.

    Raises:
        LeaderboardInputError: Naming the first failing bundle + check.
    """
    loaded: list[tuple[Path, ResultBundle, list[EpisodeResult]]] = []
    for rel in bundle_dirs:
        bundle_dir = rel if rel.is_absolute() else repo_path / rel
        checks = verify_bundle_dir(bundle_dir, repo_path=repo_path)
        failing = [c for c in checks if not c.ok]
        if failing:
            names = ", ".join(f"{c.name} ({c.detail})" for c in failing)
            msg = f"bundle {rel} failed verification: {names}"
            raise LeaderboardInputError(msg)
        bundle = ResultBundle.model_validate_json(
            (bundle_dir / "bundle.json").read_text(encoding="utf-8")
        )
        episodes: list[EpisodeResult] = [
            EpisodeResult.model_validate_json(line)
            for seed in bundle.seed_schedule.seeds
            for line in (bundle_dir / f"episodes_seed{seed}.jsonl")
            .read_text(encoding="utf-8")
            .splitlines()
            if line.strip()
        ]
        loaded.append((rel, bundle, episodes))
    return loaded


def _member_of(episode: EpisodeResult) -> str:
    member = episode.metadata.get("member") or episode.metadata.get("partner")
    return str(member) if member is not None else "<unknown>"


def compute_row(
    row_id: str,
    entries: list[tuple[Path, ResultBundle, list[EpisodeResult]]],
) -> LeaderboardRow:
    """Recompute one row's statistics from its pooled raw episodes (ADR-027).

    IQM + 95% CI via the deterministic cluster bootstrap
    (:func:`chamber.evaluation.bootstrap.cluster_bootstrap`, seeds as
    clusters — the bundle-summary convention), stress p90 over
    ``force_peak``, and the per-partner breakdown extremes.
    """
    episodes = [ep for _, _, eps in entries for ep in eps]
    by_seed: dict[int, list[float]] = {}
    for ep in episodes:
        by_seed.setdefault(ep.seed, []).append(1.0 if ep.success else 0.0)
    rng = derive_substream(
        "evaluation.leaderboard_bootstrap", root_seed=_BOOTSTRAP_ROOT_SEED
    ).default_rng()
    ci = cluster_bootstrap(by_seed, n_resamples=_N_RESAMPLES, rng=rng)
    stresses = [float(ep.force_peak) for ep in episodes if ep.force_peak is not None]
    stress_p90 = float(np.percentile(np.asarray(stresses), 90)) if stresses else float("nan")
    by_member: dict[str, list[float]] = {}
    for ep in episodes:
        by_member.setdefault(_member_of(ep), []).append(1.0 if ep.success else 0.0)
    member_means = {m: float(np.mean(np.asarray(v))) for m, v in by_member.items()}
    min_member = min(member_means, key=lambda m: member_means[m])
    max_member = max(member_means, key=lambda m: member_means[m])
    return LeaderboardRow(
        row_id=row_id,
        label=ROW_LABELS.get(row_id, ""),
        n_episodes=len(episodes),
        success_mean=ci.mean,
        success_iqm=ci.iqm,
        success_ci_low=ci.ci_low,
        success_ci_high=ci.ci_high,
        stress_p90=stress_p90,
        partner_min=(min_member, member_means[min_member]),
        partner_max=(max_member, member_means[max_member]),
        seeds=sorted(by_seed),
        bundle_paths=[str(rel) for rel, _, _ in entries],
    )


def build_rows(task_dir: Path, *, repo_path: Path) -> list[LeaderboardRow]:
    """Manifest → verified bundles → recomputed rows, in ADR-011 row order.

    Raises:
        LeaderboardInputError: Missing manifest / failing bundle.
    """
    loaded = load_verified_bundles(read_manifest(task_dir), repo_path=repo_path)
    by_row: dict[str, list[tuple[Path, ResultBundle, list[EpisodeResult]]]] = {}
    for rel, bundle, episodes in loaded:
        by_row.setdefault(row_id_for_policy(bundle.policy_id), []).append((rel, bundle, episodes))
    ordered = [r for r in ROW_ORDER if r in by_row] + sorted(set(by_row) - set(ROW_ORDER))
    return [compute_row(row_id, by_row[row_id]) for row_id in ordered]


def render_task_leaderboard(task_dir: Path, *, repo_path: Path) -> str:
    """Render one task's markdown leaderboard table (ADR-027 §Reporting rules).

    Columns per the CB-06 spec: success IQM [CI], stress p90,
    per-partner range, seeds, bundle path(s). Every number comes from
    a bundle that passed the full verify table in this process.
    """
    rows = build_rows(task_dir, repo_path=repo_path)
    lines = [
        "| Row | Label | Success IQM [95% CI] | Success mean | Stress p90 (N) "
        "| Per-partner range | Seeds | Bundles |",
        "|---|---|---|---|---|---|---|---|",
    ]
    for row in rows:
        stress = "—" if np.isnan(row.stress_p90) else f"{row.stress_p90:.1f}"
        rng_txt = (
            f"{row.partner_min[1]:.2f} ({row.partner_min[0]}) … "
            f"{row.partner_max[1]:.2f} ({row.partner_max[0]})"
        )
        label = f"*{row.label}*" if row.label else ""
        bundles = "<br>".join(f"`{p}`" for p in row.bundle_paths)
        lines.append(
            f"| {row.row_id} | {label} | "
            f"{row.success_iqm:.3f} [{row.success_ci_low:.3f}, {row.success_ci_high:.3f}] | "
            f"{row.success_mean:.3f} | "
            f"{stress} | {rng_txt} | {', '.join(str(s) for s in row.seeds)} | {bundles} |"
        )
    footnotes = [
        "",
        _ESTIMATOR_FOOTNOTE,
    ]
    anchor = _SOLVABILITY_ANCHORS.get(task_dir.name)
    if anchor:
        footnotes.insert(1, anchor)
    return "\n".join(lines + footnotes) + "\n"


def render_readme_leaderboard(*, repo_path: Path) -> str:
    """The full README leaderboard block, markers included (ADR-027; CB-02 convention).

    Renders every task directory under ``spikes/results/benchmark/``
    that commits a :data:`LEADERBOARD_MANIFEST`; with none committed
    yet, renders the honest placeholder (so the CI drift check is
    meaningful from the first commit, not from the first result).
    """
    benchmark_root = repo_path / "spikes" / "results" / "benchmark"
    sections: list[str] = []
    if benchmark_root.is_dir():
        for task_dir in sorted(p for p in benchmark_root.iterdir() if p.is_dir()):
            if (task_dir / LEADERBOARD_MANIFEST).is_file():
                sections.append(f"#### {task_dir.name}\n")
                sections.append(render_task_leaderboard(task_dir, repo_path=repo_path))
    if not sections:
        sections.append(
            "_No verified leaderboard bundles committed yet — rows land as "
            "the preregistered CB-06 campaign completes (every entry must "
            "pass `chamber-eval verify`)._\n"
        )
    body = "\n".join(sections)
    return f"{README_LEADERBOARD_BEGIN}\n{body}{README_LEADERBOARD_END}"


