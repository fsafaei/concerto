"""Partner-card renderers (ADR-009 §Decision as amended 2026-07-05; ADR-027 §Consequences).

Renders ``docs/reference/partners/<set_id>-v<N>/`` — one card per set
member plus a per-set roster index — from the set registry
(:mod:`chamber.partners.sets`) and the committed fingerprint archive
(``spikes/results/partner-fingerprints/<set_id>-v<N>/fingerprints.json``),
so the public partner surface cannot drift from code or evidence. The
``scripts/render_partner_cards.py`` wrapper does the file IO and the
``--check`` drift gating, mirroring the task-card pipeline
(:mod:`chamber.tasks.render`).

A card states what the partner does in plain language, its stratum,
split, ``partner_id`` custody hash, construction provenance (registry
class + the committed parameter box, with exact values for public
members and the withheld marker for private ones), and the behavioural
fingerprint — so a reader can tell members apart without policy access
(ADR-018/I3; published hashes, withheld parameters).
"""

from __future__ import annotations

from typing import TYPE_CHECKING, Any

from chamber.partners.sets import (
    PartnerMemberSpec,
    PartnerSetSpec,
    get_partner_set,
    list_partner_sets,
    parse_set_slug,
)

if TYPE_CHECKING:  # pragma: no cover
    from collections.abc import Mapping

_GENERATED_STAMP: str = (
    "<!-- Generated by scripts/render_partner_cards.py from chamber.partners.sets + the "
    "fingerprint archive — do not edit by hand. -->"
)

